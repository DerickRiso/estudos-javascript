// Teste no começo
var cont = 1
while (cont <= 5) {
    console.log(`Contagem ${cont}`)
    cont++
}

console.log("===========")

// Teste no final
cont = 1
do {
    console.log(`Contagem ${cont}`)
    cont++
} while (cont <= 5)

console.log("===========")

// Laço for
for (cont=1;cont <= 5;cont++ ) {
    console.log(`${cont}`)
}
